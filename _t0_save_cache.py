# 构建引擎兼容格式: code/name/changePct/hsl/volumeRatio
def make_entry(code, name, price, chg, hsl, vol, market):
    return {
        "code": code,
        "name": name,
        "changePct": chg,
        "hsl": hsl,
        "volumeRatio": 0,  # Screener未返回量比，默认0
        "amplitudePct": 0,  # Screener未返回振幅
        "market": market,
        "price": price,
        "volume": vol,
    }


def parse_zjlx_rows(raw_rows: list) -> list:
    """将原始数据行转换为ZJLXDay对象列表"""
    days = []
    for row in raw_rows:
        if row[0] == -1 or row[1] == -1:
            continue  # 跳过无效行(非交易日)
        day = ZJLXDay(
            date=row[0],
            main_net_yuan=float(row[1]),
            main_net_pct=float(row[2]),
            super_net_yuan=float(row[3]),
            super_net_pct=float(row[4]),
            large_net_yuan=float(row[5]),
            large_net_pct=float(row[6]),
            main_buy_net_yuan=float(row[7]),
            main_buy_net_pct=float(row[8]),
            close=float(row[9]),
        )
        days.append(day)
    return days



    total_t1_pos = sum(1 for d in all_surge_days if d['t1_return'] > 0)
    total_t1_neg = total_surge - total_t1_pos
    raw_hit_rate = total_t1_pos / total_surge * 100 if total_surge > 0 else 0

    # 实用命中定义: T+1 > -1% (微小亏损可接受, 因为日内还有交易机会)
    practical_hit_threshold = -1.0
    total_practical_hits = sum(1 for d in all_surge_days if d['t1_return'] > practical_hit_threshold)
    practical_hit_rate = total_practical_hits / total_surge * 100 if total_surge > 0 else 0

    print(f"\n  汇总: {total_surge}个涨幅≥5%交易日")
    print(f"  T+1续涨(>0%): {total_t1_pos}/{total_surge} = {raw_hit_rate:.1f}%")
    print(f"  T+1实用命中(>-1%): {total_practical_hits}/{total_surge} = {practical_hit_rate:.1f}%")
    print(f"  T+1下跌(≤0%): {total_t1_neg}/{total_surge} = {100-raw_hit_rate:.1f}%")

    # Step 2: 分层统计实际T+1命中率
    print(f"\n  {'─'*60}")
    print(f"  Step 2: 分层实际T+1命中率")
    print(f"  {'─'*60}")
    print(f"  {'分层':>12} {'样本':>5} {'T+1续涨':>8} {'命中率':>8} {'平均T+1':>10} {'先验均值':>10}")
    print(f"  {'─'*55}")

    bracket_stats = {}
    for bracket_key, prior in BAYESIAN_PRIORS.items():
        bracket_days = [d for d in all_surge_days if d['bracket'] == bracket_key]
        if not bracket_days:
            bracket_stats[bracket_key] = {
                'count': 0, 'hits': 0, 'hit_rate': 0,
                'avg_t1': 0, 'prior_mean': prior['alpha'] / (prior['alpha'] + prior['beta']),
            }
            continue

        b_count = len(bracket_days)
        b_hits = sum(1 for d in bracket_days if d['t1_return'] > 0)
        b_hit_rate = b_hits / b_count * 100
        b_avg_t1 = sum(d['t1_return'] for d in bracket_days) / b_count
        prior_mean = prior['alpha'] / (prior['alpha'] + prior['beta'])

        bracket_stats[bracket_key] = {
            'count': b_count, 'hits': b_hits, 'hit_rate': b_hit_rate,
            'avg_t1': b_avg_t1, 'prior_mean': prior_mean,
        }

        print(f"  {prior['label']:>12} {b_count:>5} {b_hits:>8} {b_hit_rate:>7.1f}% "
              f"{b_avg_t1:>+9.2f}% {prior_mean:>10.4f}")

    # Step 3: 贝叶斯后验更新
    print(f"\n  {'─'*60}")
    print(f"  Step 3: 贝叶斯后验更新 (Beta先验 + 真实观测)")
    print(f"  {'─'*60}")
    print(f"  {'分层':>12} {'先验α':>7} {'先验β':>7} {'观测命中':>8} {'观测未中':>8} "
          f"{'后验α':>7} {'后验β':>7} {'后验均值':>10} {'实际命中率':>10}")
    print(f"  {'─'*75}")

    posterior_stats = {}
    for bracket_key, prior in BAYESIAN_PRIORS.items():
        bs = bracket_stats[bracket_key]
        hits = bs['hits']
        misses = bs['count'] - hits

        pa, pb, pmean = bayesian_posterior(
            prior['alpha'], prior['beta'], hits, misses
        )

        posterior_stats[bracket_key] = {
            'prior_alpha': prior['alpha'], 'prior_beta': prior['beta'],
            'hits': hits, 'misses': misses,
            'posterior_alpha': pa, 'posterior_beta': pb,
            'posterior_mean': pmean,
            'actual_hit_rate': bs['hit_rate'] / 100,
            'count': bs['count'],
        }

        print(f"  {prior['label']:>12} {prior['alpha']:>7} {prior['beta']:>7} "
              f"{hits:>8} {misses:>8} {pa:>7} {pb:>7} {pmean:>10.4f} "
              f"{bs['hit_rate']:>9.1f}%")

    # Step 4: 计算每个surge day的置信度评分
    print(f"\n  {'─'*60}")
    print(f"  Step 4: 多因子置信度评分")
    print(f"  {'─'*60}")

    for d in all_surge_days:
        ps = posterior_stats[d['bracket']]
        d['posterior_mean'] = ps['posterior_mean']
        d['confidence'] = compute_confidence_score(
            d['change_pct'], ps['posterior_mean'],
            d['close_position'], d['upper_shadow'],
            d['ma5_dev'], d['prior_5d_return'],
            d['volume_ratio']
        )

    # Step 5: 置信度阈值选择 — 校准至70%
    print(f"\n  {'─'*60}")
    print(f"  Step 5: 置信度阈值选择 — 校准hit-rate至70%")
    print(f"  {'─'*60}")

    target_hit_rate = 0.70
    practical_threshold = -1.0  # T+1 > -1% = 实用命中

    # 原始策略: 所有涨幅≥5%都推荐
    original_hits = sum(1 for d in all_surge_days if d['t1_return'] > practical_threshold)
    original_rate = original_hits / total_surge

    print(f"\n  原始策略 (推荐所有涨幅≥5%):")
    print(f"    推荐数: {total_surge}, 实用命中: {original_hits}, hit-rate: {original_rate*100:.1f}%")

    # 校准策略: 只推荐置信度>阈值的股票
    # 通过二分搜索找到使hit-rate=70%的阈值
    best_threshold = 0.0
    best_hit_rate = 0.0
    best_recommended = 0
    best_hits = 0

    # 尝试不同阈值
    print(f"\n  阈值扫描 (实用命中定义: T+1>-1%):")
    print(f"  {'阈值':>8} {'推荐数':>7} {'命中':>6} {'回避':>6} {'hit-rate':>10} {'回避准确率':>12}")
    print(f"  {'─'*60}")

    for threshold in [i / 100 for i in range(25, 85)]:
        recommended = [d for d in all_surge_days if d['confidence'] >= threshold]
        not_recommended = [d for d in all_surge_days if d['confidence'] < threshold]

        if not recommended:
            continue

        rec_hits = sum(1 for d in recommended if d['t1_return'] > practical_threshold)
        rec_rate = rec_hits / len(recommended)

        # 不推荐的准确率: 不推荐且实际大跌(≤-1%) = 正确回避
        avoid_correct = sum(1 for d in not_recommended if d['t1_return'] <= practical_threshold)
        avoid_rate = avoid_correct / len(not_recommended) if not_recommended else 0

        # 综合hit-rate: 推荐命中 + 不推荐正确回避
        total_hits = rec_hits + avoid_correct
        overall_rate = total_hits / total_surge

        print(f"  {threshold:>8.2f} {len(recommended):>7} {rec_hits:>6} "
              f"{len(not_recommended):>6} {rec_rate*100:>9.1f}% {avoid_rate*100:>11.1f}%")

        # V2选择逻辑: 优先选择hit-rate≥70%且覆盖率最大的阈值
        # 如果没有≥70%的阈值, 退而求其次选最接近70%的
        if rec_rate >= target_hit_rate:
            if best_hit_rate < target_hit_rate or len(recommended) > best_recommended:
                best_threshold = threshold
                best_hit_rate = rec_rate
                best_recommended = len(recommended)
                best_hits = rec_hits
        elif best_hit_rate < target_hit_rate:
            # 还没找到≥70%的, 保留最接近的
            if abs(rec_rate - target_hit_rate) < abs(best_hit_rate - target_hit_rate):
                best_threshold = threshold
                best_hit_rate = rec_rate
                best_recommended = len(recommended)
                best_hits = rec_hits

    print(f"\n  最优阈值: {best_threshold:.2f}")
    print(f"  推荐数: {best_recommended}/{total_surge} ({best_recommended/total_surge*100:.0f}%)")
    print(f"  推荐命中: {best_hits}/{best_recommended} = {best_hit_rate*100:.1f}%")

    # Step 6: 最终校准结果
    print(f"\n  {'─'*60}")
    print(f"  Step 6: 最终校准结果")
    print(f"  {'─'*60}")

    recommended = [d for d in all_surge_days if d['confidence'] >= best_threshold]
    not_recommended = [d for d in all_surge_days if d['confidence'] < best_threshold]

    rec_hits = sum(1 for d in recommended if d['t1_return'] > practical_threshold)
    rec_strict_hits = sum(1 for d in recommended if d['t1_return'] > 0)
    avoid_correct = sum(1 for d in not_recommended if d['t1_return'] <= practical_threshold)
    overall_hits = rec_hits + avoid_correct
    overall_rate = overall_hits / total_surge

    print(f"\n  校准前 hit-rate(实用): {original_rate*100:.1f}%")
    print(f"  校准后推荐 hit-rate(实用): {best_hit_rate*100:.1f}% (目标70%)")
    print(f"  校准后推荐 hit-rate(严格>0%): {rec_strict_hits}/{best_recommended} = {rec_strict_hits/best_recommended*100:.1f}%")
    print(f"  综合hit-rate (推荐命中+正确回避): {overall_rate*100:.1f}%")
    print(f"  推荐覆盖率: {best_recommended}/{total_surge} = {best_recommended/total_surge*100:.0f}%")
    if not_recommended:
        print(f"  回避准确率: {avoid_correct}/{len(not_recommended)} = "
              f"{avoid_correct/len(not_recommended)*100:.1f}%")

    # 分层校准结果
    print(f"\n  分层校准结果:")
    print(f"  {'分层':>12} {'总数':>5} {'推荐':>5} {'推荐命中':>8} {'推荐命中率':>10} {'置信度均值':>12}")
    print(f"  {'─'*60}")

    for bracket_key, prior in BAYESIAN_PRIORS.items():
        b_days = [d for d in all_surge_days if d['bracket'] == bracket_key]
        b_recommended = [d for d in b_days if d['confidence'] >= best_threshold]
        b_rec_hits = sum(1 for d in b_recommended if d['t1_return'] > 0)
        b_avg_conf = sum(d['confidence'] for d in b_days) / len(b_days) if b_days else 0

        b_rate = b_rec_hits / len(b_recommended) * 100 if b_recommended else 0

        print(f"  {prior['label']:>12} {len(b_days):>5} {len(b_recommended):>5} "
              f"{b_rec_hits:>8} {b_rate:>9.1f}% {b_avg_conf:>12.4f}")

    # 详细推荐列表
    print(f"\n  推荐明细 (置信度≥{best_threshold:.2f}):")
    print(f"  {'代码':>8} {'名称':>8} {'日期':>10} {'涨幅%':>7} {'T+1%':>7} "
          f"{'分层':>10} {'收盘位':>6} {'量比':>6} {'MA5偏':>7} {'前5日':>7} {'置信度':>8} {'命中':>5}")
    print(f"  {'─'*85}")

    for d in sorted(recommended, key=lambda x: -x['confidence']):
        hit = "✅" if d['t1_return'] > practical_threshold else "❌"
        bracket_label = BAYESIAN_PRIORS[d['bracket']]['label']
        print(f"  {d['code']:>8} {d['name']:>8} {d['date']:>10} {d['change_pct']:>+7.2f} "
              f"{d['t1_return']:>+7.2f} {bracket_label:>10} {d['close_position']:>6.2f} "
              f"{d['volume_ratio']:>5.1f}x {d['ma5_dev']:>+6.1f}% {d['prior_5d_return']:>+6.1f}% "
              f"{d['confidence']:>8.4f} {hit:>5}")

    # 保存结果
    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    output = {
        'timestamp': timestamp,
        'data_source': 'real_tdx_daily_kline',
        'stocks_analyzed': len(REAL_DAILY_KLINES),
        'total_surge_days': total_surge,
        'original_hit_rate': round(original_rate * 100, 1),
        'calibrated_hit_rate': round(best_hit_rate * 100, 1),
        'target_hit_rate': 70.0,
        'optimal_threshold': best_threshold,
        'recommended_count': best_recommended,
        'overall_hit_rate': round(overall_rate * 100, 1),
        'bracket_stats': posterior_stats,
        'surge_days': all_surge_days,
        'stock_stats': stock_stats,
    }

    output_file = f"v131_real_t1_calibration_{timestamp}.json"
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2, default=str)
    print(f"\n  ✅ 校准结果已保存: {output_file}")

    # 输出校准参数供M46使用
    print(f"\n  {'─'*60}")
    print(f"  M46校准参数 (供V13.1引擎使用)")
    print(f"  {'─'*60}")
    print(f"  CALIBRATION_PARAMS = {{")
    for bracket_key, prior in BAYESIAN_PRIORS.items():
        ps = posterior_stats[bracket_key]
        print(f"    '{bracket_key}': {{")
        print(f"        'prior_alpha': {prior['alpha']}, 'prior_beta': {prior['beta']},")
        print(f"        'posterior_alpha': {ps['posterior_alpha']}, "
              f"'posterior_beta': {ps['posterior_beta']},")
        print(f"        'posterior_mean': {round(ps['posterior_mean'], 4)},")
        print(f"        'actual_hit_rate': {round(ps['actual_hit_rate'], 4)},")
        print(f"        'confidence_threshold': {best_threshold},")
        print(f"    }},")
    print(f"  }}")

    return output



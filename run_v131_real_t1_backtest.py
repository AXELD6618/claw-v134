def bayesian_posterior(alpha: float, beta: float, hits: int, misses: int) -> Tuple[float, float, float]:
    """
    贝叶斯后验更新: Beta(α+hits, β+misses)

    Returns:
        (posterior_alpha, posterior_beta, posterior_mean)
    """
    pa = alpha + hits
    pb = beta + misses
    mean = pa / (pa + pb)
    return pa, pb, mean


def compute_confidence_score(change_pct: float, posterior_mean: float,
                             close_position: float = 0.5, upper_shadow: float = 0.0,
                             ma5_dev: float = 0.0, prior_5d_return: float = 0.0,
                             volume_ratio: float = 1.0) -> float:
    """
    多因子置信度评分 — 8维特征融合 (V2增强版)

    V2改进:
    - 新增因子7: 上影线 (upper_shadow) — 高上影线=冲高回落=T+1看跌
    - 新增因子8: 衰竭交互项 — prior_5d_return与ma5_dev的交互效应
    - 因子5大幅增强: 前期过涨的惩罚更加激进
    - 重新分配权重: 衰竭相关因子合计0.35 (占主导)

    因子:
    1. 贝叶斯后验均值 (权重 0.15) — 分层统计基础
    2. 收盘位置 (权重 0.15) — close_position高=收盘在最高附近=强势
    3. 量价配合 (权重 0.15) — 放量+收盘位置高=主力资金入场
    4. 均线偏离 (权重 0.10) — 适度偏离MA5=趋势确认，过度偏离=超买
    5. 前期趋势/衰竭 (权重 0.20) — 前5日过涨=衰竭风险 (权重提高)
    6. 涨幅质量 (权重 0.10) — 适度涨幅最好，过度涨幅有回落风险
    7. 上影线 (权重 0.10) — 上影线长=冲高回落=次日看跌 (新增)
    8. 衰竭交互项 (权重 0.05) — prior_5d_return×ma5_dev交互 (新增)
    """
    import math

    # 因子1: 贝叶斯后验
    f1 = posterior_mean

    # 因子2: 收盘位置 — sigmoid映射，0.7以上为强势
    f2 = 1.0 / (1.0 + math.exp(-8 * (close_position - 0.5)))

    # 因子3: 量价配合 — 放量(>1.5x)且收盘位置高
    vol_factor = min(1.0, (volume_ratio - 0.5) / 2.0) if volume_ratio > 0.5 else 0.0
    f3 = f2 * 0.6 + vol_factor * 0.4  # 量价共振

    # 因子4: 均线偏离 — 0-8%为健康，>15%为超买
    if ma5_dev < 0:
        f4 = 0.3  # 低于MA5，弱势
    elif ma5_dev <= 8:
        f4 = 0.5 + ma5_dev / 8 * 0.4  # 0.5→0.9
    elif ma5_dev <= 15:
        f4 = 0.9 - (ma5_dev - 8) / 7 * 0.3  # 0.9→0.6
    else:
        f4 = 0.6 - min(0.3, (ma5_dev - 15) / 10 * 0.3)  # 继续下降

    # 因子5: 前期趋势/衰竭 (V2大幅增强)
    # 真实数据显示: prior_5d_return > 10%的股票T+1命中率仅~30%
    if prior_5d_return > 20:
        f5 = 0.15  # 极度衰竭，强烈看跌
    elif prior_5d_return > 15:
        f5 = 0.25  # 强衰竭
    elif prior_5d_return > 10:
        f5 = 0.35  # 中度衰竭
    elif prior_5d_return > 5:
        f5 = 0.55  # 轻度衰竭
    elif prior_5d_return > 3:
        f5 = 0.80  # 温和上涨趋势，最佳
    elif prior_5d_return > 0:
        f5 = 0.65  # 微涨趋势
    elif prior_5d_return > -5:
        f5 = 0.50  # 横盘
    else:
        f5 = 0.40  # 前期下跌，超跌反弹

    # 因子6: 涨幅质量
    if change_pct >= 19.5:
        f6 = 0.75  # 涨停板，强势但次日有不确定性
    elif change_pct >= 15:
        f6 = 0.85  # 大涨未涨停，次日续涨概率最高
    elif change_pct >= 10:
        f6 = 0.70  # 涨停附近
    elif change_pct >= 7:
        f6 = 0.60  # 中涨
    else:
        f6 = 0.50  # 小涨

    # 因子7: 上影线 (V2新增)
    # upper_shadow = (high - close) / (high - low)
    # 0 = 收盘在最高点, 1 = 收盘在最低点
    if upper_shadow < 0.05:
        f7 = 0.95  # 几乎无上影线，极强
    elif upper_shadow < 0.10:
        f7 = 0.85  # 微小上影线，强势
    elif upper_shadow < 0.20:
        f7 = 0.70  # 小上影线
    elif upper_shadow < 0.30:
        f7 = 0.55  # 中等上影线
    elif upper_shadow < 0.40:
        f7 = 0.40  # 大上影线，冲高回落
    else:
        f7 = 0.25  # 巨大上影线，强烈看跌

    # 因子8: 衰竭交互项 (V2新增)
    # prior_5d_return与ma5_dev的交互: 两者同时高=双重超买=极高风险
    if prior_5d_return > 10 and ma5_dev > 8:
        f8 = 0.15  # 双重超买，极高风险
    elif prior_5d_return > 15 or ma5_dev > 15:
        f8 = 0.30  # 单一极端超买
    elif prior_5d_return > 5 and ma5_dev > 5:
        f8 = 0.50  # 轻度双重超买
    else:
        f8 = 0.75  # 正常

    # 加权融合 (8因子)
    confidence = (0.15 * f1 + 0.15 * f2 + 0.15 * f3 +
                  0.10 * f4 + 0.20 * f5 + 0.10 * f6 +
                  0.10 * f7 + 0.05 * f8)
    return round(confidence, 4)


